int	ft_atoi(const char *str)
{
	int	i;
	int	res;
	int	n;

	res = 0;
	i = 0;
	n = 1;
	while ((str[i] <= 13 && str[i] >= 9) || str[i] == 32)
		i++;
	if (str[i] == 45)
	{
		n = -1;
		i++;
	}
	else if (str[i] == 43)
		i++;
	while (str[i] <= 57 && str[i] >= 48)
	{
		res = res * 10 + (str[i] - 48);
		i++;
	}
	return (res * n);
}
