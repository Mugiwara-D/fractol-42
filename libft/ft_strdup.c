#include "libft.h"

char	*ft_strdup(const char *src)
{
	int		i;
	char	*dst;

	i = ft_strlen(src) + 1;
	dst = malloc(sizeof(char) * i);
	if (!dst)
		return (NULL);
	i = 0;
	while (src[i])
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
